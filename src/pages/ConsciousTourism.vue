function downloadRandomFile() {
  // Lista przykładowych treści plików
  const fileContents = [
    'Raport 2025\n\nDziałania ekologiczne w Twoim obiekcie:\n- Zmniejszenie zużycia wody\n- Instalacja paneli słonecznych',
    'Raport 2025\n\nPodsumowanie:\n- Liczba zrealizowanych inicjatyw: 5\n- Wsparcie dla lokalnej społeczności: Tak',
    'Raport 2025\n\nZrównoważona turystyka:\n- Certyfikaty ekologiczne: Tak\n- Redukcja śladu węglowego: 15%',
  ];
  // Wybierz losowy tekst z listy
  const randomContent =
    fileContents[Math.floor(Math.random() * fileContents.length)];
  // Utwórz blob z tekstem
  const blob = new Blob([randomContent], { type: 'text/plain' });
  // Utwórz URL dla pliku
  const url = window.URL.createObjectURL(blob);
  // Utwórz element <a> do pobierania
  const a = document.createElement('a');
  a.href = url;
  a.download = 'raport.txt'; // Nazwa pliku
  a.click();
  // Zwolnij zasoby URL
  window.URL.revokeObjectURL(url);
}

</script>
